mystd::pair<CPacketLengthExMgr::enumFixedPacketRet,int> CPacketLengthExMgr::IsFixedPacket(const int in_Header) const
{
	LENGTHMAPEX::const_iterator iter = m_LengthMap.find(in_Header);
	if( iter == m_LengthMap.end() )
		return mystd::pair<CPacketLengthExMgr::enumFixedPacketRet,int>(CPacketLengthExMgr::UNKNOWN, 4);

	if( iter->second.m_Len == -1 )
		return mystd::pair<CPacketLengthExMgr::enumFixedPacketRet,int>(CPacketLengthExMgr::VARIABLE, iter->second.m_Min);
	else
	if( iter->second.m_Len >= 0 )
		return mystd::pair<CPacketLengthExMgr::enumFixedPacketRet,int>(CPacketLengthExMgr::FIXED, iter->second.m_Len);
	else
	{
		return mystd::pair<CPacketLengthExMgr::enumFixedPacketRet,int>(CPacketLengthExMgr::UNKNOWN, 4);
	}
}


BOOL CPacketLengthExMgr::GetReplayFactor(const int in_Header) const
{
	LENGTHMAPEX::const_iterator iter = m_LengthMap.find(in_Header);
	if( iter == m_LengthMap.end() )
		return FALSE;

	return iter->second.m_ReplayFactor;
}


void CPacketLengthExMgr::Add(int in_Header, int in_Length, int in_MinLength, int in_ReplayFactor)
{
	m_LengthMap.insert(mystd::pair<const int,CData>(in_Header, CData(in_Length, in_MinLength, in_ReplayFactor)));
}


